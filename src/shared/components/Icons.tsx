export function AppIcon({
  name,
  className = APP_ICON_CLASS_NAMES.base,
  'aria-hidden': ariaHidden = true,
  viewBox,
  ...props
}: AppIconProps) {
  const icon = APP_ICON_MAP[name];
  const resolvedClassName = [APP_ICON_CLASS_NAMES.base, className].filter(Boolean).join(' ');

  return (
    <svg
      viewBox={viewBox ?? icon.viewBox ?? '0 0 16 16'}
      className={resolvedClassName}
      fill="currentColor"
      aria-hidden={ariaHidden}
      {...props}
    >
      {icon.paths.map((path, index) => (
        <path
          key={`${name}-${index}`}
          d={path.d}
          fillRule={path.fillRule}
          clipRule={path.clipRule}
        />
      ))}
    </svg>
  );
}
